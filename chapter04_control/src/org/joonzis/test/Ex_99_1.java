package org.joonzis.test;

public class Ex_99_1 {
	public static void main(String[] args) {
		//Q1. 세개의 과목 국어 89점, 영어 99점, 수학 78점인 점수와 총 점을 출력
		int sub1 = 89;
		int sub2 = 99;
		int sub3 = 78;
		
		System.out.println("국어 점수는 " + sub1 + "점 입니다." );
		System.out.println("영어 점수는 " + sub2 + "점 입니다." );
		System.out.println("수학 점수는 " + sub3 + "점 입니다." );
		System.out.println("총 점수는 " + (sub3+sub1+sub2) + "점 입니다." );
		
	}

}
