package org.joonzis.test;

public class EX101_test {
	public static void main(String[] args) {
//		Q. 10 ~ 1 역순으로 출력하기
		
		//1. for
		for (int i = 10; i>0; i--) {
			
			System.out.println(i);
		}
		//2. while
		int num = 10;
		
		while (num>=1) {
			
			System.out.println(num);
			num--;
		}
		
	}

}
